              <td>&nbsp;</td>
              <td><p>All papers dealing with issues within the scope of  the Congress: <strong>Physical education in the 21 century &ndash; pupils&rsquo; competencies,</strong> are cordially invited.&nbsp; The papers may be  written in Croatian or English, with the Abstract and key words in  English. <br />
                <br />
Participants are invited to submit their papers for  either a podium or poster presentation.
<p class="tekstOkvir"><em> <br />&nbsp;The accepted papers will be published  in a Book of Proceedings before the Congress. <br /> <br /></em></p>
&nbsp;
All submitted papers will be internationally  peer-reviewed and evaluated. Following the review, the papers will be accepted,  conditionally accepted, or rejected. The authors will be notified of the  acceptance before 15 March 2011. Participants may have only one paper as first  authors and one additional paper as co-authors. Only the accepted papers will  be published in the Book of Proceedings, provided that the submitting author  (the first author) has paid the fee. <br />
<br />
Contributors are stipulated to keep their papers  within the determined framework of up to a maximum of 6 pages (including title,  names of authors, body of the text, tables, figures or any other graphical  presentations, references). The abstract and key words are not included in the  framework of 6 pages and should not exceed 150 words (900 characters).&nbsp;&nbsp; </p>
                <div>                  Please, consider the deadlines for paper  submission (1 February 2011) via registration form which may be obtained  at&nbsp; the Congress web&nbsp; site <a href="http://www.hrks.hr/fiep2011">www.hrks.hr/fiep2011</a> as  MS Word document with the accurate information about the author (first name,  surname, address, institution, contact telephone number, e-mail address). &nbsp;Congress participation application should also  be made using the application form that may be obtained at&nbsp; the Congress web&nbsp; site <a href="http://www.hrks.hr/fiep2011">www.hrks.hr/fiep2011</a> &nbsp; </div>
                <p><strong>POSTERS</strong><u>;</u> preferably measuring 60 x 84   cm, will be on display during the Congress. They should  be structured in the same manner as the presentations, but SHOULD be easily  legible from a distance of at least 2 meters. <br />
                  <strong>TYPEFACES;</strong> Times New Roman style (TNR) is recommended with  character size 12 pts  in the body of the text, spacing at 1.5; Arial Narrow style (AN) in tables with  character size 10 pts,  single-spaced. <br />
                  <strong>HEADINGS;</strong> The title of the paper should be printed in capital  letters (TNR, 14 pts,  bold, spacing at 1.5), centred; a single blank line following, then come the  full names of the author(s) and the name of their respective institution (TNR,  11pts, bold, italics). </p>
              <strong>Official  languages: English and Croatian.&nbsp;  Simultaneous interpretation is provided!</strong></td>
